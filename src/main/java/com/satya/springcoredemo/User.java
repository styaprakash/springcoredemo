package com.satya.springcoredemo;

import org.springframework.stereotype.Component;


//here spring can't use component because spring is confused as the name and age can't be initiated by
//@Component
public class User {
    private String name;
    private int age;

    public User(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
